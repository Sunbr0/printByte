/*
 * charByte: Reads the binary value stored for the char given and prints
 * the value to stdout.
 * Args: A char to be evaluated
 */
void charByte(char byte);
/*
 * shortByte: Reads the binary value stored for the short given and prints
 * the value to stdout.
 * Args: A short to be evaluated
 */
void shortByte(short byte);
/*
 * intByte: Reads the binary value stored for the integer given and prints
 * the value to stdout.
 * Args: An integer to be evaluated
 */
void intByte(int byte);
/*
 * longByte: Reads the binary value stored for the long given and prints
 * the value to stdout.
 * Args: A long to be evaluated
 */
void longByte(long byte);
